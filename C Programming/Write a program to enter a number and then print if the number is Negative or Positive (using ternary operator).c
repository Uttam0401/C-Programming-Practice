//Write a program to enter a number and then print if the number is Negative or Positive (using ternary operator)
#include<stdio.h>
#include<stdio.h>
int main()
{
    int a;
    printf("Enter Any Numbers:-");
    scanf("%d",&a);
    if(a >= 0)
    {
        printf("The Numbers Is Positive");

    }
    else
    {
        printf("The Numbers Is Negative");
    }
    return 0;
}